#include "../ft_printf.h"

int	ft_putstr(char *str)
{
	int	total;

	total = 0;
	if (str == (void *)0)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (*str)
	{
		write(1, str++, 1);
		total += 1;
	}
	return (total);
}
